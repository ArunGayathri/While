package Com.While;

public class Switch {
public static void main(String[] args) {
	
	int month=3;
	
	switch(month)
	{
	case 1:
			System.out.println("1.Jan");
		break;
	case 2:
			System.out.println("2.Feb");
		break;
	case 3:
		  System.out.println("3.march");
		break;
	case 4:
			System.out.println("4.April");
		break;
		default:System.out.println("Invalid on Month");
	}
	  
}
}
